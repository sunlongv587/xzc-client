const {ccclass, property} = cc._decorator;

@ccclass
export default class SplashScene extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null;

    @property
    text: string = 'hello';

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {}

    start () {
        this.label.string = "这是加载场景。。。"
        setTimeout(() => {
            cc.director.loadScene('StartScene', () => {
                console.log("加载完毕！！！")
            })
        }, 200)
    }

    // update (dt) {}
}
